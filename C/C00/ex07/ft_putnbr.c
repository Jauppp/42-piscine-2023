#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	print;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		print = '0' + nb % 10;
		write(1, &print, 1);
	}
	else
	{
		print = '0' + nb;
		write(1, &print, 1);
	}
}
